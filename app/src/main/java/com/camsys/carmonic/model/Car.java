package com.camsys.carmonic.model;

public class Car {

    String color;
    String brand;
    String model;
    String year;
    String isssues;


    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getIsssues() {
        return isssues;
    }

    public void setIsssues(String isssues) {
        this.isssues = isssues;
    }


}
